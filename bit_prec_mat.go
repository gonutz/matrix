package matrix

// NewBitPrecMat returns a matrix with the given size. It can contain only
// values that fit in the given number of bits.
func NewBitPrecMat(w, h, bits int) BitPrecMat {
	if bits < 1 || bits > 31 {
		panic("NewBitPrecMat32: bits must be in the range [1..31]")
	}
	// add one padding because we later treat these as uint64s
	size := (w*h*bits+31)/32 + 1
	var mask uint32
	for i := 0; i < bits; i++ {
		mask |= 1 << uint(i)
	}
	return BitPrecMat{
		w:    w,
		h:    h,
		bits: bits,
		mask: mask,
		data: make([]uint32, size),
	}
}

// BitPrecMat is a 2D matrix with numbers of an arbitrary fixed bit length.
type BitPrecMat struct {
	w, h int
	bits int
	mask uint32
	data []uint32
}

// Size returns the width and height of the matrix, i.e. the number of columns
// and rows, in that order.
func (m *BitPrecMat) Size() (w, h int) {
	return m.w, m.h
}

// Set sets the matrix value at the given position, x is the column and y is the
// row, starting top-left at 0,0. You must make sure that the given value is
// representable in the precision of the matrix.
func (m *BitPrecMat) Set(x, y int, to uint32) {
	bitOfs := (x + y*m.w) * m.bits
	dataI, maskI := bitOfs/32, bitOfs%32
	to64 := uint64(to) << (64 - uint64(m.bits) - uint64(maskI))
	mask64 := bitMasks[m.bits][maskI]
	oldData64 := uint64(m.data[dataI])<<32 | uint64(m.data[dataI+1])
	newData64 := (oldData64 & ^mask64) | to64
	m.data[dataI] = uint32(newData64 & 0xFFFFFFFF00000000 >> 32)
	m.data[dataI+1] = uint32(newData64 & 0xFFFFFFFF)
}

// Get returns the value at the given position, x is the column and y is the
// row, starting top-left at 0,0.
func (m *BitPrecMat) Get(x, y int) uint32 {
	bitOfs := (x + y*m.w) * m.bits
	dataI, maskI := bitOfs/32, bitOfs%32
	mask64 := bitMasks[m.bits][maskI]
	oldData64 := (uint64(m.data[dataI])<<32 | uint64(m.data[dataI+1])) & mask64
	return uint32(oldData64 >> uint64(64-m.bits-maskI))
}

